package com.cesarandres.ps2link.dbg.content;

public class CharacterDirectiveObjective {

	private String character_id;
	private String directive_id;
	private String objective_group_id;
	private String objective_id;
	private String state_data;
	private String status;
	
	public String getCharacter_id() {
		return character_id;
	}
	public void setCharacter_id(String character_id) {
		this.character_id = character_id;
	}
	public String getDirective_id() {
		return directive_id;
	}
	public void setDirective_id(String directive_id) {
		this.directive_id = directive_id;
	}
	public String getObjective_group_id() {
		return objective_group_id;
	}
	public void setObjective_group_id(String objective_group_id) {
		this.objective_group_id = objective_group_id;
	}
	public String getObjective_id() {
		return objective_id;
	}
	public void setObjective_id(String objective_id) {
		this.objective_id = objective_id;
	}
	public String getState_data() {
		return state_data;
	}
	public void setState_data(String state_data) {
		this.state_data = state_data;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
}
